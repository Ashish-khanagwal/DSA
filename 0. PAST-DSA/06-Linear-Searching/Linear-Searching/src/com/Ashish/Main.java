package com.Ashish;

public class Main {
    public static void main(String[] args) {
        System.out.println("Let's learn about linear searching");
        /*
           Linear search is a simple searching algorithm that sequentially checks each element in a list until a match is
           found or the entire list has been traversed. It is also known as sequential search
        */

        /*
           Basic Idea:
            - Start from the first element of the list.
            - Compare each element with the target element.
            - If the element matches the target, return its index.
            - If the element doesn't match, move to the next element.
            - Repeat steps 2-4 until the end of the list is reached or the target element is found.
        */

        /*
            Time complexity of linear search is O(1).
        */
    }
}